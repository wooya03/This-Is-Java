package ch02.sec06;

public class StringEx {
    public static void main(String[] args) {
        String name = "홍길동";
        System.out.println(name);

        String str = "나는 \"자바\"를 배웁니다.";
        System.out.println(str);

        str = "번호\t이름\t직업";
        System.out.println(str);

        System.out.print("나는\n자바를\n배웁니다\n");
    }
}
